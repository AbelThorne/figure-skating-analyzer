class GenericTableScraper(BaseSiteScraper):
    """
    Generic scraper that looks for HTML tables containing competitor data.

    Heuristic approach:
    - Finds tables that likely contain rankings (columns: rank/name/club/nation/score)
    - Follows links to sub-pages (categories) and scrapes those too
    - Best-effort: works reasonably well on statically generated sites
    """

    _NAT_RE = re.compile(r"^[A-Z]{2,3}$")
    _YEAR_RE = re.compile(r"\b(19[5-9]\d|20[0-2]\d)\b")
    _RANK_RE = re.compile(r"^\d{1,3}\.?$")
    _NUM_RE = re.compile(r"^\d{1,3}$")
    _SCORE_RE = re.compile(r"^\d+(?:[.,]\d+)?$")

    _HEADER_MAP = {
        "rang": "rank", "rank": "rank", "pl": "rank", "place": "rank",
        "clt": "rank",
        "num": "starting_number", "n°": "starting_number", "no": "starting_number",
        "bib": "starting_number", "départ": "starting_number",
        "nom": "name", "name": "name", "athlete": "name", "patineur": "name",
        "patineurs": "name", "concurrent": "name",
        "club": "club", "société": "club", "societe": "club",
        "nation": "nationality", "nat": "nationality", "pays": "nationality",
        "né": "birth_year", "naissance": "birth_year", "born": "birth_year",
        "an.": "birth_year",
        "catégorie": "category", "cat": "category", "category": "category",
        "segment": "segment",
        "points": "total_score", "score": "total_score", "total": "total_score",
        "pts": "total_score",
    }

    def can_handle(self, url: str) -> bool:
        return True  # fallback

    async def scrape(self, url: str, client: httpx.AsyncClient) -> list[ScrapedCompetitor]:
        pages = await self._collect_pages(url, client)
        competitors: list[ScrapedCompetitor] = []
        seen: dict[str, ScrapedCompetitor] = {}

        for _page_url, html, category_hint in pages:
            soup = BeautifulSoup(html, "html.parser")
            for c in self._extract_from_page(soup, category_hint):
                key = normalize_name(c.name)
                if key not in seen:
                    seen[key] = c
                    competitors.append(c)
                else:
                    _merge_competitor(seen[key], c)

        return competitors

    async def _collect_pages(
        self, url: str, client: httpx.AsyncClient
    ) -> list[tuple[str, str, str | None]]:
        pages: list[tuple[str, str, str | None]] = []
        visited: set[str] = set()
        base_path = url.rsplit("/", 1)[0] + "/"

        async def fetch_page(page_url: str, category: str | None) -> None:
            if page_url in visited:
                return
            visited.add(page_url)
            try:
                resp = await client.get(page_url, follow_redirects=True)
                if resp.status_code != 200:
                    return
                # Best-effort encoding
                try:
                    html = resp.content.decode(resp.encoding or "windows-1252", errors="replace")
                except Exception:
                    html = resp.text
                pages.append((page_url, html, category))

                soup = BeautifulSoup(html, "html.parser")
                for a in soup.find_all("a", href=True):
                    href = a["href"]
                    if not href or href.startswith("#") or href.startswith("mailto"):
                        continue
                    abs_href = urljoin(page_url, href)
                    if abs_href.startswith(base_path) and abs_href not in visited:
                        if re.search(r"\.(htm|html)$", abs_href, re.IGNORECASE):
                            hint = a.get_text(strip=True) or category
                            await fetch_page(abs_href, hint)
            except Exception as exc:
                logger.debug("Skipping %s: %s", page_url, exc)

        await fetch_page(url, None)
        return pages

    def _extract_from_page(
        self, soup: BeautifulSoup, category_hint: str | None
    ) -> list[ScrapedCompetitor]:
        competitors: list[ScrapedCompetitor] = []

        category = category_hint
        if not category:
            for tag in soup.find_all(["h1", "h2", "h3", "h4", "caption"]):
                text = _clean_text(tag.get_text())
                if text:
                    category = text
                    break

        for table in soup.find_all("table"):
            rows = table.find_all("tr")
            if len(rows) < 2:
                continue

            headers = [
                _strip_accents(th.get_text(strip=True)).lower().strip(" .:*/")
                for th in rows[0].find_all(["th", "td"])
            ]
            col_map = self._map_columns(headers)

            if "name" not in col_map:
                continue

            for row in rows[1:]:
                cells = row.find_all(["td", "th"])
                if not cells:
                    continue
                c = self._parse_row(cells, col_map, category)
                if c:
                    competitors.append(c)

        return competitors

    def _map_columns(self, headers: list[str]) -> dict[str, int]:
        col_map: dict[str, int] = {}
        for i, h in enumerate(headers):
            for keyword, field_name in self._HEADER_MAP.items():
                if keyword in h and field_name not in col_map:
                    col_map[field_name] = i
                    break
        return col_map

    def _parse_row(
        self, cells: list[Tag], col_map: dict[str, int], category: str | None
    ) -> ScrapedCompetitor | None:
        def cell_text(field: str) -> str | None:
            idx = col_map.get(field)
            if idx is None or idx >= len(cells):
                return None
            return cells[idx].get_text(strip=True) or None

        raw_name = cell_text("name")
        if not raw_name or len(raw_name) < 3:
            return None
        name = _title_case_name(raw_name)

        club = cell_text("club")
        nationality = cell_text("nationality")
        if nationality and not self._NAT_RE.match(nationality):
            nationality = None

        birth_year: int | None = None
        by_raw = cell_text("birth_year")
        if by_raw:
            m = self._YEAR_RE.search(by_raw)
            if m:
                birth_year = int(m.group(1))

        rank: int | None = None
        rank_raw = cell_text("rank")
        if rank_raw:
            m = re.match(r"^(\d{1,3})\.?$", rank_raw.strip())
            if m:
                rank = int(m.group(1))

        starting_number: int | None = None
        sn_raw = cell_text("starting_number")
        if sn_raw and self._NUM_RE.match(sn_raw):
            starting_number = int(sn_raw)

        total_score: float | None = None
        score_raw = cell_text("total_score")
        if score_raw:
            score_raw = score_raw.replace(",", ".")
            if self._SCORE_RE.match(score_raw):
                try:
                    total_score = float(score_raw)
                except ValueError:
                    pass

        segment = cell_text("segment")
        cat_col = cell_text("category")

        return ScrapedCompetitor(
            name=name,
            nationality=nationality,
            club=club,
            birth_year=birth_year,
            category=cat_col or category,
            segment=segment,
            starting_number=starting_number,
            rank=rank,
            total_score=total_score,
        )


# ---------------------------------------------------------------------------
# Shared helpers
# ---------------------------------------------------------------------------

def normalize_name(name: str) -> str:
    """Normalize a skater name for fuzzy matching between HTML and PDF sources."""
    name = _strip_accents(name).lower()
    name = re.sub(r"[^a-z\s]", "", name)
    return " ".join(name.split())


def _strip_accents(text: str) -> str:
    return "".join(
        c for c in unicodedata.normalize("NFD", text)
        if unicodedata.category(c) != "Mn"
    )


def _clean_text(text: str) -> str:
    """Collapse whitespace and strip leading/trailing spaces."""
    return " ".join(text.split())


def _title_case_name(name: str) -> str:
    """
    Convert an ALL-CAPS French name to title case.

    French names frequently appear in ALL CAPS on competition result pages.
    Examples:
        "DUPONT MARIE"     → "Dupont Marie"
        "DE LA FONTAINE"   → "de la Fontaine"   (particle lowercased)
        "Dupont Marie"     → "Dupont Marie"      (already mixed-case: untouched)

    Only converts if the name is all-uppercase; mixed-case names are left as-is
    to avoid corrupting names that were already entered correctly.
    """
    stripped = name.strip()
    # If not all-uppercase, return as-is (preserve intentional mixed case)
    alpha_chars = [c for c in stripped if c.isalpha()]
    if not alpha_chars or not all(c.isupper() for c in alpha_chars):
        return stripped

    # French lowercase particles
    _PARTICLES = {"de", "du", "des", "le", "la", "les", "d", "l", "van", "von", "da"}

    parts = stripped.split()
    result = []
    for i, part in enumerate(parts):
        lower = part.lower()
        if i > 0 and lower in _PARTICLES:
            result.append(lower)
        else:
            result.append(part.capitalize())
    return " ".join(result)


def _merge_competitor(target: ScrapedCompetitor, source: ScrapedCompetitor) -> None:
    """Fill in missing fields in target from source."""
    for f in (
        "nationality", "club", "birth_year", "category",
        "segment", "starting_number", "rank", "total_score",
    ):
        if getattr(target, f) is None and getattr(source, f) is not None:
            setattr(target, f, getattr(source, f))
    target.extra.update({k: v for k, v in source.extra.items() if k not in target.extra})


# ---------------------------------------------------------------------------
# Registry
# ---------------------------------------------------------------------------

_scrapers: list[BaseSiteScraper] = [
    FrenchIJSScraper(),   # checked first; falls back to generic if can_handle() is False
    GenericTableScraper(),
]


def register_scraper(scraper: BaseSiteScraper, priority: bool = True) -> None:
    """Register a new site scraper. Set priority=True to insert before existing ones."""
    if priority:
        _scrapers.insert(0, scraper)
    else:
        _scrapers.append(scraper)


def _select_scraper(url: str) -> BaseSiteScraper:
    for s in _scrapers:
        if s.can_handle(url):
            return s
    return _scrapers[-1]


